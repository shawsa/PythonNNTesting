from BackPropNN import *
nn = BackPropNN(2,1,1,3,.05)
#nn.print_network()
print("Outputs")
print("(0,0)", nn.output((0,0)))
print("(1,0)", nn.output((1,0)))
print("(0,1)", nn.output((0,1)))
print("(1,1)", nn.output((1,1)))
print("Training...")
for i in range(30000):
	nn.back_prop((0,0),(0))
	nn.back_prop((1,0),(1))
	nn.back_prop((0,1),(1))
	nn.back_prop((1,1),(0))
#nn.print_network()
print("Outputs")
print("(0,0)", nn.output((0,0)))
print("(1,0)", nn.output((1,0)))
print("(0,1)", nn.output((0,1)))
print("(1,1)", nn.output((1,1)))